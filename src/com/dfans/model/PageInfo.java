package com.dfans.model;

public class PageInfo {
	
	private String page = "";
	
	private String count = "";

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}
	
}
